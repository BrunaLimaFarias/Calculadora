function Somar(){

    var input1 = parseInt(document.getElementById("input1").value);
    var input2 = parseInt(document.getElementById("input2").value);
    total = input1 + input2;

    alert(total);
       
}

function Subtrair(){

    var input1 = parseInt(document.getElementById("input1").value);
    var input2 = parseInt(document.getElementById("input2").value);
    total = input1 - input2;

    alert(total);
       
}

function Multiplicar(){

    var input1 = parseInt(document.getElementById("input1").value);
    var input2 = parseInt(document.getElementById("input2").value);
    total = input1 * input2;

    alert(total);
       
}

function Dividir(){

    var input1 = parseInt(document.getElementById("input1").value);
    var input2 = parseInt(document.getElementById("input2").value);
    total = input1 / input2;

    alert(total);
       
}